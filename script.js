function createTable(data) {
    const tableContainer = document.getElementById('table-container');
    const table = document.createElement('table');
    const tbody = document.createElement('tbody');

    function createRow(element, level = 0) {
        const row = document.createElement('tr');
        const cell = document.createElement('td');
        
        cell.innerText = element.name;
        cell.style.fontWeight = element.font_weight;
        cell.style.fontStyle = element.font_style;
        cell.style.textAlign = element.text_align;
        cell.style.fontSize = `${element.font_size}px`;
        
        if (element.place === 'top') {
            const th = document.createElement('th');
            th.colSpan = 2;
            th.innerText = element.name;
            th.style.fontWeight = element.font_weight;
            th.style.fontStyle = element.font_style;
            th.style.textAlign = element.text_align;
            th.style.fontSize = `${element.font_size}px`;
            row.appendChild(th);
        } else {
            row.appendChild(cell);
        }

        tbody.appendChild(row);

        if (element.children && element.children.length > 0) {
            element.children.forEach(child => createRow(child, level + 1));
        }
    }

    data.forEach(element => createRow(element));
    table.appendChild(tbody);
    tableContainer.appendChild(table);
}

